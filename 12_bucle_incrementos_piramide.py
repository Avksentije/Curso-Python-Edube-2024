bloques_disponibles = int(input("Ingresa el número de bloques: "))

#ingresamos un contador
altura = 0
bloques_piramide = 0


while bloques_piramide <= bloques_disponibles:
    
    altura += 1                         
    bloques_piramide += altura          #iniciamos el contador while True
    
altura -= 1                             #when False, salimos del ciclo y regresamos al nivel completo
        

print("La altura de la pirámide:", altura)

